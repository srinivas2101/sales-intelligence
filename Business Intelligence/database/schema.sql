CREATE DATABASE IF NOT EXISTS `business_intelligence`;
USE `business_intelligence`;

CREATE TABLE IF NOT EXISTS products (
    id            INT AUTO_INCREMENT PRIMARY KEY,
    name          VARCHAR(255) NOT NULL,
    category      VARCHAR(100),
    sub_category  VARCHAR(100),
    price         DECIMAL(10,2) NOT NULL,
    cost_price    DECIMAL(10,2),
    stock         INT DEFAULT 0,
    unit          VARCHAR(50) DEFAULT 'pack',
    expiry_days   INT DEFAULT NULL,
    reorder_point INT DEFAULT 10,
    risk_score    INT DEFAULT 0,
    created_at    TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at    TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS sales (
    id             INT AUTO_INCREMENT PRIMARY KEY,
    bill_no        VARCHAR(50),
    customer_name  VARCHAR(255) DEFAULT 'Walk-in',
    customer_phone VARCHAR(20),
    product_id     INT NOT NULL,
    quantity       INT NOT NULL,
    unit_price     DECIMAL(10,2) NOT NULL,
    discount       DECIMAL(10,2) DEFAULT 0,
    total_amount   DECIMAL(10,2) NOT NULL,
    profit         DECIMAL(10,2) DEFAULT 0,
    payment_method VARCHAR(50) DEFAULT 'Cash',
    sale_date      DATE NOT NULL,
    created_at     TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (product_id) REFERENCES products(id)
);

CREATE TABLE IF NOT EXISTS customers (
    id               INT AUTO_INCREMENT PRIMARY KEY,
    name             VARCHAR(255),
    phone            VARCHAR(20) UNIQUE,
    address          TEXT,
    segment          VARCHAR(50) DEFAULT 'New',
    total_spent      DECIMAL(10,2) DEFAULT 0,
    visit_count      INT DEFAULT 0,
    first_visit      DATE,
    last_visit       DATE,
    engagement_score INT DEFAULT 50,
    created_at       TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS upload_log (
    id              INT AUTO_INCREMENT PRIMARY KEY,
    filename        VARCHAR(255),
    sale_date       DATE,
    bills_count     INT DEFAULT 0,
    items_processed INT DEFAULT 0,
    total_revenue   DECIMAL(12,2) DEFAULT 0,
    uploaded_at     TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS expiry_tracking (
    id                INT AUTO_INCREMENT PRIMARY KEY,
    product_id        INT NOT NULL,
    batch_qty         INT NOT NULL,
    manufactured_date DATE,
    expiry_date       DATE NOT NULL,
    units_sold        INT DEFAULT 0,
    units_wasted      INT DEFAULT 0,
    created_at        TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (product_id) REFERENCES products(id)
);

CREATE TABLE IF NOT EXISTS predictions (
    id               INT AUTO_INCREMENT PRIMARY KEY,
    product_id       INT NOT NULL,
    predicted_sales  DECIMAL(10,2),
    predicted_units  INT,
    confidence_score DECIMAL(5,2),
    factors          JSON,
    prediction_date  DATE,
    target_date      DATE,
    created_at       TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (product_id) REFERENCES products(id)
);

