// Deterministic pseudo-random based on product id seed
const seededRand = (seed, min, max) => {
  const x = Math.sin(seed * 9301 + 49297) * 233280;
  const r = x - Math.floor(x);
  return Math.floor(min + r * (max - min + 1));
};

const p = (name, cat, sub, price, cost, stock, expiryDays, unit='pack', reorder=20) => {
  const id = _id++;
  const expRange     = CAT_EXPIRY[cat]  || [30, 365];
  const stkRange     = CAT_STOCK[cat]   || [20, 300];
  const catReorder   = CAT_REORDER[cat] || reorder;
  const realExpiry   = seededRand(id * 3 + 7,  expRange[0], expRange[1]);
  const realStock    = seededRand(id * 5 + 13, stkRange[0], stkRange[1]);
  const realReorder  = catReorder;

  // Calculate realistic risk score based on actual values
  let riskScore = 0;
  if (realStock <= 0) riskScore += 50;
  else if (realStock < realReorder * 0.4) riskScore += 35;
  else if (realStock < realReorder)       riskScore += 20;
  else if (realStock < realReorder * 2)   riskScore += 8;

  if (realExpiry <= 2)  riskScore += 40;
  else if (realExpiry <= 7)  riskScore += 25;
  else if (realExpiry <= 14) riskScore += 10;

  const margin = price > 0 ? ((price - cost) / price) * 100 : 0;
  if (margin < 5)  riskScore += 15;
  else if (margin < 10) riskScore += 7;

  riskScore = Math.min(riskScore, 100);

  return { id, name, category: cat, sub_category: sub, price, cost_price: cost,
           stock: realStock, unit, expiry_days: realExpiry,
           reorder_point: realReorder, risk_score: riskScore };
};

